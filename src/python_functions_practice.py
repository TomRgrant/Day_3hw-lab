def number_to_full_month_name(num):
    if num == 1:
        return "January"
    elif num == 3:
        return "March"
    elif num == 9:
        return "September"

def number_to_short_month_name(num):
    if num == 1:
        return "Jan"
    elif num == 4:
        return "Apr"
    elif num == 10:
        return "Oct"

def volume_of_cube(length):
    return length**3

def reverse_string(str):
    return str [::-1]

def fahrenheit_to_celsius(fahrenheit):
    celcius = round(((fahrenheit - 32) * 0.5556))
    return celcius














































































